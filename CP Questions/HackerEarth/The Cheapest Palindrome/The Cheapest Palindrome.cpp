#include<bits/stdc++.h>
using namespace std;
#define ll long long 
 
int main(){
    
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    ll t;
    cin>>t;
    while(t--){

        //Taking string as input 

        string s;
        cin>>s;
        ll p=s.length()-1,x,y;
        ll q=s.length()/2;
        ll ans=0,r=0;

        //Assigning mapping values of costs
        map<char,ll> m;
        cin>>x>>y;
        m['a']=x;
        m['b']=y;
        m['/']=0;

        // Calculating ans
 
        ll k=min(m['a'],m['b']);
        for(int i=0;i<q;i++){
        if(s[i]!=s[p-i]){
         if(s[i]=='/'||s[p-i]=='/'){
          ans=ans+m[s[i]]+m[s[p-i]];
         }
         else{
             cout<<-1<<"\n";
             r=1;
             break;
         }
        }
        else{
            if(s[i]==s[p-i]&&s[p-i]=='/')
            ans=ans+(2*k);
            
        }
        }
        if(r==0)
        cout<<ans<<"\n";
    }
}